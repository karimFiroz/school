১৯১২ সালে বঙ্গভঙ্গ রহিত হয়। পূর্ববঙ্গের মুসলমানরা এ পদক্ষেপে চরমভাবে হতাশ হয়। মুসলিম স্বার্থ রক্ষার উদ্দেশ্যে জনমত গঠনের জন্য বঙ্গীয় প্রাদেশিক মুসলিম লীগ গঠন করা হয়।<br /><br />

১৯১৯ সালে মন্টেগু-চেমসফোর্ড রিপোর্ট সীমিত প্রতিনিধিত্বশীল সরকার প্রবর্তনের মাধ্যমে স্বায়ত্তশাসনের প্রক্রিয়া শুরু করে। পৃথক নির্বাচন ব্যবস্থা বহাল থাকে। কংগ্রেস এ পদ্ধতির বিরোধিতা করায় অনেক মুসলিম জাতীয়তাবাদী ক্ষুব্ধ হন এবং সাম্প্রদায়িক রাজনীতিতে যোগদান করেন।<br /><br /><br />

১৯২৩ সালে বঙ্গীয় হিন্দু-মুসলমান নেতৃবর্গের মধ্যে সম্পাদিত বেঙ্গল প্যাক্ট নামীয় চুক্তির শর্ত মোতাবেক বঙ্গীয় মুসলমানদের আপেক্ষিক পশ্চাদপদতা দূর করার লক্ষ্যে তাদের জন্য অধিক সংখ্যক সরকারি চাকরি বরাদ্ধ রাখার অঙ্গীকার করা হয়। মুসলমানরা এ চুক্তিতে সন্তোষ প্রকাশ করে। কিন্তু এ চুক্তির প্রণেতা সি. আর দাসের মৃত্যুর (১৯২৫) পর হিন্দুদের চাপের মুখে চুক্তি বাস্তবায়নের পথ রুদ্ধ হয়। এর ফলে পূর্বেকার অনেক অসাম্প্রদায়িক মুসলিম নেতা সাম্প্রদায়িক রাজনীতি শুরু করেন।<br /><br />

১৯৩৫ সালের ভারত সরকার আইনে মুসলমানদের জন্য পৃথক নির্বাচনের ব্যবস্থা করা হয় এবং এভাবে মুসলিম সংখ্যাগরিষ্ঠ এলাকার মুসলমানদের নিজস্ব সরকার গঠনের সুযোগ সৃষ্টি করা হয়। কংগ্রেস সাম্প্রদায়িক প্রতিনিধিত্বের অজুহাত দেখিয়ে এ আইনের অধীনে নির্বাচন বয়কট করার হুমকি দেয়। তবে পরবর্তীতে তারা ঠিকই নির্বাচনে অংশগ্রহণ করে। এর ফলে হিন্দু-মুসলমান সম্পর্কের আরো অবনতি হয়।<br /><br />

১৯৩৭  সালের সাধারণ নির্বাচনে আসন জয়ের ব্যাপারে বাংলায় কংগ্রেসের স্থান ছিল প্রথম, দ্বিতীয় স্থান অধিকার করে মুসলিম লীগ এবং কৃষক প্রজা পার্টি পায় তৃতীয় স্থান। পৃথক নির্বাচন ব্যবস্থার প্রতিবাদে কংগ্রেস সরকার গঠন করতে অস্বীকৃতি জানায়। তাদের পরিকল্পনা ছিল সংখ্যালঘু দলগুলো একজোট হয়ে যে সরকার গঠন করবে সেটাকে অকার্যকর করে সরকারকে শাসনতান্ত্রিক সংস্কারে বাধ্য করা। তবে কৃষক প্রজা পার্টি নেতা এ.কে ফজলুল হক মুসলিম লীগ এবং কিছু দলছুট সদস্যের সমন্বয়ে মন্ত্রিসভা গঠন করতে সক্ষম হন।<br /><br />

১৯৩৮-৩৯  সালে কংগ্রেসের অসহযোগিতার কারণে এ.কে ফজলুল হক নির্বাচনী ইশতেহারের প্রতিশ্রুতি অনুযায়ী জমিদারী প্রথা বিলোপ করতে ব্যর্থ হন। এতে মুসলমান কৃষক সমাজ কংগ্রেসের সদিচ্ছা সম্পর্কে সন্দিগ্ধ হয়ে উঠে।<br /><br />

১৯৪০  সালে লাহোরে অনুষ্ঠিত মুসলিম লীগের সাধারণ সভায় গৃহীত প্রস্তাবে দ্বিজাতিতত্ত্ব ঘোষণা করা হয় এবং ভারতের মুসলিম সংখ্যাগরিষ্ঠ এলাকাগুলিকে নিয়ে একটি ‘স্বাধীন রাষ্ট্র’ ঘোষণার জন্য ব্রিটিশ সরকারের প্রতি আহবান জানানো হয়।<br /><br />

১৯৪৩ সালে খাজা নাজিমউদ্দীনকে প্রধানমন্ত্রী করে মুসলিম লীগ মন্ত্রিসভা গঠন করলে সাম্প্রদায়িক রাজনীতি আরো শক্তিশালী হয়ে উঠে।<br /><br />

১৯৪৬ সালের সাধারণ নির্বাচনে মুসলিম লীগ পাকিস্তানের ধারণাকে প্রধান ইস্যু হিসেবে তুলে ধরে। আইন পরিষদে মুসলিম লীগ নিরঙ্কুশ সংখ্যাগরিষ্ঠ দল হিসেবে আবির্ভূত হয়ে বাংলায় মুসলিম জাতীয়তাবাদী রাজনীতির জয় ঘোষণা করে। হোসেন শহীদ সোহরাওয়ার্দী প্রধানমন্ত্রী হিসেবে মুসলিম লীগের মন্ত্রিসভা গঠন করেন।<br /><br />

১৯৪৬ সালের ১৬ আগস্টকে প্রত্যক্ষ সংগ্রাম দিবস ঘোষণা করে সোহরাওয়ার্দী এক হরতাল ডেকে দিবসটিকে সরকারি ছুটির দিন ঘোষণা করেন। হিন্দু ও মুসলিম সম্প্রদায়ের হাজার হাজার লোকের মৃত্যু ও পঙ্গুত্ব বরণের মধ্য দিয়ে হরতালের পরিসমাপ্তি ঘটে। একই ধারাবাহিকতায় হিন্দু মহাসভা সাম্প্রদায়িকতার ভিত্তিতে বঙ্গভঙ্গ দাবি করে। অচিরেই কংগ্রেস দল এই দাবি সমর্থন করে।<br /><br />

১৯৪৬ সালে ক্যাবিনেট মিশন পরিকল্পনাধীনে সাম্প্রদায়িক ভিত্তিতে বাংলা ভাগ করে পৃথক স্বাধীন রাষ্ট্র গঠনের সিদ্ধান্ত হয়।<br /><br />

১৯৪৭ সালের ১৪ আগস্ট বঙ্গদেশকে পূর্ববঙ্গ এবং পশ্চিমবঙ্গ এই দুইভাগে বিভক্ত করা হয়। পূর্ববঙ্গ পরিণত হয় পাকিস্তানের পূর্বাঞ্চলে।<br /><br />

বাঙালি জাতীয়তাবাদের ভিত্তিতে বাংলাদেশ রাষ্ট্রের অভ্যুদয় যাত্রা<br /><br />

১৯৪৮  সালে পাকিস্তানের স্থপতি মোহাম্মদ আলী জিন্নাহর পূর্ববঙ্গ সফর। ঢাকা বিশ্ববিদ্যালয়ে তিনি ঘোষণা দেন যে, উর্দুই হবে পাকিস্তানের একমাত্র রাষ্ট্রভাষা। উপস্থিত ছাত্ররা তাৎক্ষণিকভাবে এ প্রস্তাবের প্রতিবাদ করে এবং সংখ্যাগরিষ্ঠ মানুষের ভাষা বাংলাকে পাকিস্তানের রাষ্ট্রভাষা করার দাবি জানায়।<br /><br />

১৯৪৯  সালে বঙ্গীয় মুসলিম লীগ দ্বিখন্ডিত হয়ে মুসলিম লীগের উদারপন্থী ও বামপন্থীরা আওয়ামী মুসলিম লীগ নামে একটি নতুন রাজনৈতিক দল গঠন করে এবং মওলানা আবদুল হামিদ খান ভাসানীকে এর সভাপতি নির্বাচিত করে।<br /><br />

১৯৫২  সালের ২১ ফেব্রুয়ারি, শহীদ দিবস। বাংলাকে পাকিস্তানের রাষ্ট্রভাষা করার দাবি তুলে ধরতে গিয়ে ছাত্ররা ঢাকা বিশ্ববিদ্যালয়ের আমতলায় এক প্রতিবাদ সভায় মিলিত হয় এবং ফৌজদারী কার্যবিধির ১৪৪ ধারা অমান্য করে শোভাযাত্রা বের করে। পুলিশ গুলিবর্ষণ করে। এতে কয়েকজন ছাত্র নিহত হয়। এ ঘটনাকে কেন্দ্র করে সরকারের বিরুদ্ধে প্রতিরোধ আন্দোলনের সূত্রপাত হয়।<br /><br />

১৯৫৩  সালে এ.কে ফজলুল হকের কৃষক প্রজা পার্টির পুনরুজ্জীবিত রূপ কৃষক শ্রমিক পার্টি (কেএসপি) তার একুশ দফা কর্মসূচি ঘোষণা করে। লাহোর প্রস্তাবের মর্মবাণী অনুযায়ী পূর্ব-পাকিস্তানের স্বায়ত্তশাসন অর্জন করা ছিল এ কর্মসূচির মূল দাবি।<br /><br />

১৯৫৩  সালের নভেম্বর মাসে মুসলিম লীগ সরকারের বিরুদ্ধে যুক্তফ্রন্ট নামে একটি নির্বাচনী জোট গঠন করা হয়। আওয়ামী লীগ, কেএসপি, নেজামে ইসলাম এবং গণতন্ত্রী দলের সমন্বয়ে গঠিত হয় যুক্তফ্রন্ট। এই নির্বাচনী জোট পূর্ববঙ্গের স্বায়ত্তশাসন প্রতিষ্ঠা, আঞ্চলিক বৈষম্যের অবসান এবং বাংলাকে রাষ্ট্রভাষার মর্যাদা দানের ওপর মূল দৃষ্টি নিবদ্ধ করে ২১-দফার একটি নির্বাচনী মেনিফেস্টো প্রকাশ করে।<br /><br />

১৯৫৪ সালের ১১ মার্চ পূর্ববঙ্গে অনুষ্ঠিত প্রাদেশিক নির্বাচনে যুক্তফ্রন্ট ক্ষমতাসীন মুসলিম লীগকে সম্পূর্ণ পর্যুদস্ত করে জয়লাভ করে। কৃষক শ্রমিক পার্টি প্রধান এ.কে ফজলুল হক পূর্ববঙ্গের মুখ্যমন্ত্রী নির্বাচিত হন।<br /><br />

১৯৫৪  সালের ৩১ মে মুখ্যমন্ত্রী ফজলুল হক এবং আওয়ামী লীগের সাধারণ সম্পাদক শেখ মুজিবুর রহমানকে বরখাস্ত করে গৃহবন্দী রাখা হয়। অভিযোগ, তাঁরা পূর্ববঙ্গকে পাকিস্তান থেকে বিচ্ছিন্ন করার লক্ষ্যে ষড়যন্ত্রে লিপ্ত।<br /><br />

১৯৫৪  সালের ১৯ অক্টোবর বগুড়ার জয়পুরহাটে আওয়ামী লীগের কনভেনশনে দলের নাম আওয়ামী মুসলিম লীগ থেকে মুসলিম শব্দ বাদ দিয়ে ’আওয়ামী লীগ’ করা হয়। এ পরিবর্তন দলটির পাকিস্তানি মুসলিম জাতীয়তাবাদী নীতি পরিত্যাগ করে ধর্মনিরপেক্ষ বাঙ্গালী জাতীয়তাবাদী নীতি অনুসরণের ইঙ্গিত বহন করে।<br /><br />

১৯৫৭  সালের ৩ এপ্রিল প্রাদেশিক পরিষদে আঞ্চলিক স্বায়ত্তশাসন প্রতিষ্ঠার প্রস্তাব গৃহীত হয়।<br /><br />

১৯৫৮ সালের ২৭ অক্টোবর জেনারেল আইয়ুব খান প্রেসিডেন্ট জেনারেল ইস্কান্দার মীর্যাকে উৎখাত করে সামরিক আইন জারী করেন। সামরিক আইনে সকল নির্বাচনী ব্যবস্থা বিলুপ্ত ও রাজনীতি নিষিদ্ধ করা হয় এবং প্রধান রাজনৈতিক নেতাদের নির্বাচনে প্রতিযোগিতার অযোগ্য ঘোষণা করে একাধিক সামরিক বিধিনিষেধ জারি করা হয়।<br /><br />

১৯৬২ সালের জানুয়ারি মাসে হোসেন শহীদ সোহরাওয়ার্দীকে নিরাপত্তা আইনে গ্রেফতার করা হয়। এর প্রতিবাদে বিশ্ববিদ্যালয়ের ছাত্র সমাজ এবং জনতা বিক্ষোভ প্রদর্শন করে। ১৯৬২ সালের ৮ ফেব্রুয়ারি আওয়ামী লীগের সাধারণ সম্পাদক শেখ মুজিবুর রহমানসহ অনেক নেতা-কর্মীকে বন্দী করা হয়। ২২ সেপ্টেম্বর পুলিশ প্রতিবাদ দিবস পালনরত ছাত্রদের ওপর গুলিবর্ষণ করে।<br /><br />

১৯৬৩-১৯৬৫  বাঙালি বুদ্ধিজীবী ও অর্থনীতিবিদগণ পাকিস্তানের দুই অংশের মধ্যে ক্রমবর্ধমান বৈষম্য তুলে ধরেন। তারা তথ্যভিত্তিক যুক্তি দেন, কিভাবে পূর্ব পাকিস্তানের অর্জিত বৈদেশিক মুদ্রা পশ্চিম পাকিস্তানের স্বার্থে ব্যবহূত হচ্ছে, এবং স্বায়ত্তশাসন পেলে কিভাবে পূর্ব পাকিস্তান স্বয়ং সম্পূর্ণ ও সমৃদ্ধ হয়ে উঠতে পারে।<br /><br />

১৯৬৬ সালের ৬ ফেব্রুয়ারি, পশ্চিম পাকিস্তানের বিরোধী দলের নেতৃবর্গ লাহোরে একটি জাতীয় কনভেনশন আহবান করেন। আওয়ামী লীগ প্রধান শেখ মুজিবুর রহমান দলের অন্যান্য শীর্ষ নেতাদের নিয়ে ওই কনভেনশনে যোগদান করেন এবং এ সম্মেলনের সমর্থন আদায়ের জন্য তাঁর যুগান্তকারী ৬-দফা দাবি উপস্থাপন করেন। কিন্তু পশ্চিম পাকিস্তানের বিরোধী দলগুলো ৬-দফা ফর্মূলাকে একটি উদ্ভট প্রস্তাব বলে প্রত্যাখ্যান করেন। পশ্চিম পাকিস্তানের পত্রপত্রিকায় ৬-দফা কর্মসূচি নিয়ে বৈরী লেখালেখি হয় এবং শেখ মুজিবকে একজন বিচ্ছিন্নতাবাদী হিসেবে তুলে ধরা হয়। শেখ মুজিব সম্মেলন বয়কট করে ঢাকায় প্রত্যাবর্তন করেন।<br /><br />

১৯৬৬ সালের ১৮ এপ্রিল, জরুরি বিধিবলে শেখ মুজিবুর রহমানকে গ্রেফতার করা হয়। শেখ মুজিবের ৬-দফা সনদের সমর্থনে পূর্ব পাকিস্তানে সরকারের বিরুদ্ধে জন-প্রতিরোধ আন্দোলন গড়ে ওঠে।<br /><br />

১৯৬৭ সালের আগরতলা ষড়যন্ত্র মামলা, শেখ মুজিবুর রহমান এবং আরো চৌত্রিশ জন জননেতা, সশস্ত্র বাহিনীর সদস্য, ব্যবসায়ী এবং আমলাকে সশস্ত্র অভ্যুত্থানের মাধ্যমে পূর্ব-পাকিস্তানকে স্বাধীন করার কথিত ষড়যন্ত্র মামলায় জড়ানো হয়।<br /><br />

১৯৬৮ সালে আগরতলা ষড়যন্ত্র মামলার বিরুদ্ধে গণআন্দোলন শুরু হয়। বামপন্থী রাজনীতিবিদ এবং ছাত্ররা স্বাধীন পূর্ববাংলা প্রজাতন্ত্রের নাম দিয়ে কর্মসূচি ঘোষণা করে। তাদের শ্লোগান ছিল, ‘স্বাধীন পূর্ববাংলা প্রজাতন্ত্র কায়েম কর’।<br /><br />

১৯৬৯ সালের ১ জানুয়ারি থেকে গণআন্দোলন কার্যত একটি গণঅভ্যুত্থানে পরিণত হয়। সর্বদলীয় ছাত্র সংগ্রাম পরিষদ ১১-দফা সনদ ঘোষণা করে যার লক্ষ্য ছিল কার্যত পূর্ব বাংলার স্বাধীনতা অর্জন। ছাত্র সংগ্রাম পরিষদ কতিপয় শ্লোগানের মাধ্যমে বাঙালি জাতীয়তাবাদের ধারণাকে সুস্পষ্ট করেছিল। যেমন, ‘জাগো, জাগো, বাঙালি জাগো, বীর বাঙালি অস্ত্র ধরো, বাংলাদেশ স্বাধীন করো’; ‘তোমার দেশ, আমার দেশ, বাংলাদেশ বাংলাদেশ’। ছাত্ররা বাংলাদেশের জাতীয় পতাকা এবং জাতীয় সঙ্গীতও নির্বাচন করেন। তাঁদের জাগরণী শ্লোগান ছিল ‘জয়বাংলা’।<br /><br />

২২ ফেব্রুয়ারি  দুর্বার গণআন্দোলনের মুখে আইয়ুব সরকার আগরতলা ষড়যন্ত্র মামলাটি প্রত্যাহার করে নেয় এবং শেখ মুজিবুর রহমানকে নিঃশর্ত মুক্তি দিয়ে গণদাবির নিকট মাথা নত করে।<br /><br />

২৩ ফেব্রুয়ারি  রমনা রেসকোর্স ময়দানে শেখ মুজিবুর রহমানকে ছাত্র সংগ্রাম পরিষদ আয়োজিত সংবর্ধনা। শেখ মুজিবকে স্বাগত জানানোর জন্য সেখানে সমবেত জনতা জনসমুদ্রের রূপ নেয়। সভার সভাপতি ছাত্রনেতা তোফায়েল আহমদ শেখ মুজিবুর রহমানকে সম্মানসূচক ‘বঙ্গবন্ধু’ খেতাবে ভূষিত করার প্রস্তাব করলে হর্ষোৎফুলল জনতা বিপুল উৎসাহ উদ্দীপনায় তা সমর্থন করে। পরবর্তী মিডিয়ার রিপোর্টেও জননেতাকে এই সম্মানসূচক খেতাবের জন্য অভিনন্দিত করা হয়। শেখ মুজিব দেশবাসী ও ‘বিপ্লবী জনতার’ প্রতি ধন্যবাদ জানিয়ে এই সম্মাননা গ্রহণ করেন।<br /><br />

১০ মার্চ  সরকার আয়োজিত রাওয়ালপিন্ডি গোলটেবিল বৈঠকে শেখ মুজিবুর রহমান আওয়ামী লীগের ফেডারেল ব্যবস্থা ৬-দফা ফর্মূলা পেশ করেন। পশ্চিম পাকিস্তানের রাজনীতিবিদরা এ ৬-দফা ফর্মূলাকে পাকিস্তান ভাঙ্গার জন্য একটি ছদ্মকৌশল বলে আখ্যায়িত করেন।<br /><br />

২৫ মার্চ  গোলটেবিল বৈঠক ব্যর্থ হওয়ায় এবং আইয়ূব বিরোধী আন্দোলন অপ্রতিরোধ্য গতিতে চলতে থাকায় ফিল্ড মার্শাল আইয়ূব খান পদত্যাগ করে জেনারেল ইয়াহিয়ার কাছে ক্ষমতা হস্তান্তর করেন। সামরিক আইন জারী করা হয়। জাতীয় এবং প্রাদেশিক নির্বাচন অনুষ্ঠানের জন্য ইয়াহিয়া খান একটি সুনির্দিষ্ট তারিখ ঘোষণা করেন।<br /><br />

১৯৭০ সালের ২৮ অক্টোবর রেডিও পাকিস্তান বেতারযোগে নির্বাচনী বক্তৃতায় বঙ্গবন্ধু তাঁর ছয়দফা ভিত্তিক ফেডারেল ব্যবস্থার ধারণাটি নতুন করে উত্থাপন করেন এবং সবাইকে তাঁর ধারণাটি গ্রহণ করতে এবং ৬-দফার ভিত্তিতে পাকিস্তানের জন্য নতুন একটি শাসনতন্ত্র প্রণয়নে তাঁকে সহযোগিতা করার আহবান জানান।<br /><br />

২৩ নভেম্বর  পল্টন ময়দানের এক জনসভায় মওলানা আবদুল হামিদ খান ভাসানী ঘোষণা করেন যে, অতীতের ঘটনাবলি এবং ১২ নভেম্বরের ঘূর্নিঝড় দুর্গত মানুষের প্রতি পাকিস্তান সরকারের ষড়যন্ত্রমূলক উদাসীনতা থেকে তাঁর এই বিশ্বাস জন্মেছে যে, রাষ্ট্র হিসেবে পাকিস্তান অবাস্তব এবং লক্ষ্যহীন হয়ে পড়েছে। তিনি পূর্ব-পাকিস্তানের জনসাধারণকে পাকিস্তানের সঙ্গে সম্পর্ক ছিন্ন করার এবং এ ভূখন্ডকে একটি স্বাধীন রাষ্ট্রে পরিণত করার দাবি জানান। তিনি ‘পাকিস্তান জিন্দাবাদ’ এই গতানুগতিক শ্লোগান না দিয়ে বক্তৃতা শেষ করেন ‘পূর্ব পাকিস্তান জিন্দাবাদ’ শ্লোগান দিয়ে।<br /><br />

২৬ নভেম্বর  ঘূর্ণিঝড় দুর্গত এলাকা সফর শেষে ঢাকায় ফিরে জাতীয় এবং আন্তর্জাতিক সাংবাদিকদের উপস্থিতিতে এক সংবাদ সম্মেলনে শেখ মুজিবুর রহমান ঘূর্নিঝড় দুর্গত মানুষের পাশে দাঁড়াতে পাকিস্তান সরকারের চরম ব্যর্থতা তুলে ধরেন এবং পরিশেষে ঘোষণা করেন যে, এটা যত না ক্ষমতাসীন সরকারের ব্যর্থতা তার চাইতে বড় ব্যর্থতা খোদ পাকিস্তান রাষ্ট্রের। বঙ্গবন্ধু তাঁর বক্তব্য শেষ করেন এই বলে, পূর্ব-পাকিস্তানকে সম্ভব হলে ব্যালটের মাধ্যমে এবং প্রয়োজন হলে বুলেটের দ্বারা স্বায়ত্তশাসন অর্জন করতে হবে।<br /><br />

৪ ডিসেম্বর  সকল রাজবন্দীর মুক্তির দাবিতে আয়োজিত এক জনসভায় ছাত্রলীগ দুটি শ্লোগান তোলে: ক) কৃষক-শ্রমিক অস্ত্র ধরো, বাংলাদেশ স্বাধীন কর, খ) গণবাহিনী গড়ে তোল, বাংলাদেশ স্বাধীন কর। পল্টন ময়দানে ন্যাপ (ভাসানী), জাতীয় লীগ (আতাউর রহমান) এবং জামায়াত-ই-উলামায়ে ইসলাম (পীর মুহসিনউদ্দীন) আয়োজিত একটি যৌথ রাজনৈতিক জনসভায় মওলানা ভাসানী লাহোর প্রস্তাবের ভিত্তিতে স্বাধীন পূর্ব পাকিস্তান প্রতিষ্ঠার দাবি জানান। এই জনসভায় বক্তৃতাকালে আতাউর রহমান খান এবং পীর মুহসিনউদ্দীনও মওলানা ভাসানীর এ প্রস্তাবের প্রতি পূর্ণ সমর্থন জ্ঞাপন করেন।<br /><br />

৭ এবং ১৭ ডিসেম্বর  সাধারণ নির্বাচন অনুষ্ঠিত হয়। জনগণ ৬-দফা এবং ১১-দফা কর্মসুচির সমর্থনে ভোট দান করে। আওয়ামী লীগ পূর্ব পাকিস্তান আইনসভার ৩০০ আসনের মধ্যে ২৮৮ আসনে এবং জাতীয় পরিষদের ৩০০ আসনের মধ্যে ১৬৭ আসনে জয়লাভ করে। তৎসত্ত্বেও আওয়ামী লীগ অথবা পাকিস্তান পিপলস পার্টি এদের কেউই নিজেদের প্রদেশের বাইরে একটি আসনেও জয়লাভ করতে পারে নি। এ থেকে এটাই প্রমাণিত হয় যে, পাকিস্তানের দুই প্রদেশ রাজনৈতিক দিক থেকে একে অপর থেকে সম্পূর্ণ বিচ্ছিন্ন হয়ে পড়েছে।<br /><br />

১৯৭১ সালের ৩ জানুয়ারি  ৬-দফা এবং ১১-দফা কর্মসূচি বাস্তবায়নের শপথ গ্রহণ করার উদ্দেশ্যে সকল নির্বাচিত প্রতিনিধি রমনায় মিলিত হন। বঙ্গবন্ধু এ শপথ গ্রহণ অনুষ্ঠান পরিচালনা করেন। প্রতিনিধিরা তাদের উপর অর্পিত জনগণের আস্থা ৬-দফা এবং ১১-দফা প্রশ্নে ঐক্যবদ্ধ থাকার এবং কখনও দোদুল্যমানতার পরিচয় না দেবার জন্য প্রতিশ্রুতিবদ্ধ হন।<br /><br />

১২-১৩ জানুয়ারি  জেনারেল ইয়াহিয়া ঢাকায় আসেন এবং শেখ মুজিব ও অন্যান্য নেতার সঙ্গে রাজনৈতিক সমস্যা নিয়ে আলোচনা করেন। শেখ মুজিব সাংবাদিকদের জানান যে, তাঁর এবং ইয়াহিয়া খানের মধ্যে আন্তরিক ও সন্তোষজনক আলোচনা হয়েছে। ইয়াহিয়া খানও ঢাকা ত্যাগের সময় একই অভিমত ব্যক্ত করেন এবং শেখ মুজিবকে ‘পাকিস্তানের ভাবী প্রধানমন্ত্রী’ বলে সম্বোধন করেন।<br /><br />

২৭-২৮ জানুয়ারি  ভূট্টো তাঁর প্রধান সহকর্মীদের নিয়ে ঢাকায় আসেন এবং ক্ষমতা ভাগবণ্টনের ব্যাপারে শেখ মুজিব এবং তাঁর অন্যান্য সহকর্মীদের সাথে আলোচনা করেন। এসব আলাপ আলোচনা থেকে ইতিবাচক কোনো ফল বেরিয়ে আসে নি। ভূট্টো সাংবাদিকদের কাছে এ মন্তব্য করে ঢাকা ত্যাগ করেন, ‘আরও আলাপ আলোচনার প্রয়োজন আছে।’ মুজিব সর্বশেষ ১৫ ফেব্রুয়ারির মধ্যে ঢাকায় জাতীয় সংসদের অধিবেশন আহবানের দাবি জানান।<br /><br />

১৩ ফেব্রুয়ারি  ইয়াহিয়া খান জাতীয় সংসদের প্রথম অধিবেশন শুরুর ঘোষণা দেন। তিনি ঘোষণা দেন যে, ঢাকায় জাতীয় সংসদের অধিবেশন শুরু হবে ৩ মার্চ।<br /><br />

১৫ ফেব্রুয়ারি  ভুট্টো ঘোষণা দেন যে, ৬-দফা ইস্যুতে পূর্বাহ্নে একটি সমঝোতায় উপনীত না হওয়া পর্যন্ত তাঁর দল ঢাকায় জাতীয় পরিষদের অধিবেশনে যোগদান করবে না।<br /><br />

২১ ফেব্রুয়ারি  বঙ্গবন্ধু শেখ মুজিবুর রহমান শহীদ মিনার থেকে ঘোষণা দেন যে, বাঙালির অধিকার ও স্বার্থের বিরুদ্ধে যেকোন চক্রান্তের মোকাবিলার জন্য জনগণকে প্রস্ত্তত থাকতে হবে।<br /><br />

২২ ফেব্রুয়ারি  প্রেসিডেন্ট ইয়াহিয়া তার মন্ত্রিসভা ভেঙ্গে দিয়ে মিলিটারি জেনারেলদের সাথে আলোচনায় বসেন এবং নিজের মতো করে সমস্যার সমাধান করার অঙ্গীকার ব্যক্ত করেন।<br /><br />

২৪ ফেব্রুয়ারি  রাজনৈতিক দৃশ্যপটে নাটকীয় পরিবর্তনের পরিপ্রেক্ষিতে বঙ্গবন্ধু একটি সাংবাদিক সম্মেলন ডাকেন। তিনি কুচক্রী সেনাবাহিনীর কার্যকলাপের তীব্র সমালোচনা করেন। তিনি জনগণকে স্মরণ করতে বলেন কিভাবে সেনাবাহিনী সেই ১৯৫৪ সাল থেকে এদেশের মাটিতে গণতান্ত্রিক প্রতিষ্ঠানকে শেকড় গাড়তে বাধা প্রদান করেছে। তিনি ঘোষণা দেন যে, জনগণ তাদের গণতান্ত্রিক ও মানবাধিকার রক্ষা এবং স্বায়ত্তশাসন প্রতিষ্ঠার জন্য শেষ পর্যন্ত সংগ্রাম চালিয়ে যাবে।<br /><br />

২৮ ফেব্রুয়ারি  বঙ্গবন্ধু শেখ মুজিবুর রহমান জাতীয় পরিষদের সদস্যদের ঢাকা অধিবেশনে যোগ দেয়ার এবং পাকিস্তানের জন্য একটি গণতান্ত্রিক শাসনতন্ত্র প্রণয়নে সাহায্য করার আহবান জানান। একই দিন ভুট্টো হুমকি দেন যে, তিনি এবং তার দল ঢাকা অধিবেশন বর্জন করবে।<br /><br />

১ মার্চ  প্রেসিডেন্ট ইয়াহিয়া খান ৩ মার্চ ঢাকায় অনুষ্ঠেয় জাতীয় পরিষদের নির্ধারিত অধিবেশন বাতিল ঘোষণা করেন। তাৎক্ষণিকভাবে ঢাকায় প্রতিরোধ আন্দোলন শুরু হয়ে যায়। হাজার হাজার মানুষ হোটেল পূর্বানীর সামনে জমায়েত হয়। সেখানে তখন বঙ্গবন্ধুর সভাপতিত্বে আওয়ামী লীগের কাউন্সিল বৈঠক চলছিল। উত্তেজিত জনতা বঙ্গবন্ধুর কাছে অবিলম্বে বাংলাদেশের স্বাধীনতা ঘোষণার দাবি জানান। বঙ্গবন্ধু জনতাকে সংযত থাকতে উপদেশ দেন এবং ৭ মার্চ পর্যন্ত প্রতিবাদ কর্মসূচি চালিয়ে যাওয়ার ঘোষণা দিয়ে বলেন যে, ৭ মার্চ তিনি রেসকোর্স ময়দানে জাতির উদ্দেশে একটি গুরুত্বপূর্ণ নীতি নির্ধারণী ঘোষণা দেবেন।<br /><br />

২ মার্চ  শহরের সব এলাকা এবং শহরতলি থেকে ছাত্রজনতা ঢাকা বিশ্ববিদ্যালয়ের বটতলায় জমায়েত হতে থাকে। জনতা স্বাধীনতার শ্লোগান দিতে থাকে এবং ঐকতানে গাইতে থাকে স্বাধীনতার সঙ্গীত। ছাত্রনেতারা (নূরে আলম সিদ্দিকী, এএসএম আবদুর রব, শাহজাহান সিরাজ এবং আবদুল কুদ্দুস মাখন) জনতার উদ্দেশে বক্তব্য রাখেন এবং বঙ্গবন্ধুর নেতৃত্বে বাংলাদেশের স্বাধীনতা ঘোষণা করেন। তারপর ছাত্রলীগ নেতা এবং ঢাকা বিশ্ববিদ্যালয় কেন্দ্রীয় ছাত্র সংসদের (ডাকসুর) সহ-সভাপতি এএসএম আবদুর রব জয়বাংলা শ্লোগান এবং তুমুল করতালির মধ্যে স্বাধীনতার পতাকা উত্তোলন করেন।<br /><br />

৩ মার্চ  দেশব্যাপী হরতাল পালনের ঘোষণা দেয়া হয়। ঢাকা বিশ্ববিদ্যালয়ের শিক্ষকগণ বটতলায় জমায়েত হয়ে বঙ্গবন্ধু শেখ মুজিবুর রহমানের নেতৃত্বে স্বাধীনতা সংগ্রামের সাথে তাদের একাত্মতা ঘোষণা করেন। বঙ্গবন্ধু পল্টন ময়দানে এক বিশাল জনসভায় ভাষণ দেন এবং ৭ মার্চ পর্যন্ত তাঁর সংগ্রামের কর্মসূচি ঘোষণা করেন। কিন্তু জনতা তাঁর কাছে তাৎক্ষণিক এবং সরাসরি স্বাধীনতার ঘোষণা দেবার দাবি জানায়।<br /><br />

ছাত্রনেতা এএসএম আবদুর রব, আবদুল কুদ্দুস মাখন, নূরে আলম সিদ্দিকী এবং শাহজাহান সিরাজের যৌথ নেতৃত্বে স্বাধীন বাংলা ছাত্র সংগ্রাম পরিষদ বাংলাদেশের স্বাধীনতা ঘোষণা করে একটি ইস্তেহার বিলি করে। ইস্তেহারের বিষয় ছিল: ‘জয় বাংলা: স্বাধীনতার ঘোষণা। বাংলাদেশের স্বাধীনতা এতদ্বারা ঘোষণা করা হলো। বাংলাদেশ এখন এক স্বাধীন ও সার্বভৌম দেশ।’ স্বাধীন বাংলা ছাত্র সংগ্রাম পরিষদ বাংলাদেশের জন্য একটি জাতীয় সংগীত নির্বাচিত করে। সেটি হলো রবীন্দ্রনাথের কবিতা ও সংগীত ‘ও আমার সোনার বাংলা....।’<br /><br />

৪ মার্চ  দেশব্যাপী পূর্ণ হরতাল পালিত হয়। সকল ব্যবসা-বাণিজ্য, সরকারি প্রতিষ্ঠান এবং যোগাযোগ ব্যবস্থা সম্পূর্ণ অচল হয়ে যায়। মওলানা আবদুল হামিদ খান ভাসানী অনতিবিলম্বে স্বাধীন সার্বভৌম বাংলাদেশ প্রতিষ্ঠার দাবি জানান।<br /><br />

৫ মার্চ  এএসএম আবদুর রব, আবদুল কুদ্দুস মাখন এবং অন্যান্য ছাত্রনেতার নেতৃত্বে ছাত্ররা ঢাকায় এক লাঠি মিছিল বের করে। ড. আহমদ শরীফের নেতৃত্বে বুদ্ধিজীবী এবং পেশাজীবীরা স্বাধীনতা আন্দোলনের প্রতি আনুগত্য প্রকাশ করে শপথ গ্রহণ করেন।<br /><br />

৬ মার্চ  প্রেসিডেন্ট ইয়াহিয়া ঘোষণা দেন যে, ঢাকায় জাতীয় পরিষদ অধিবেশন বসবে ২৫ মার্চ। কিন্তু আওয়ামী লীগ ও অন্যান্য রাজনৈতিক দল আগের মতই অসহযোগ আন্দোলন পুরোপুরি এবং প্রবলভাবে চালিয়ে যাওয়ার সঙ্কল্প ব্যক্ত করে।<br /><br />

৭ মার্চ  বঙ্গবন্ধু শেখ মুজিবুর রহমানের বক্তব্য শোনার জন্য রমনা রেসকোর্সে লক্ষ লক্ষ লোকের সমাগম। বেশিরভাগ লোকের হাতে ছিল প্রতিরোধের প্রতীক লাঠি। বিরামহীন জয়বাংলা শ্লোগান। সর্বত্র গগনভেদী শ্লোগান: ‘জয় বাংলা; আপোষ না সংগ্রাম, সংগ্রাম সংগ্রাম; আমার দেশ তোমার দেশ, বাংলাদেশ বাংলাদেশ; পরিষদ না রাজপথ, রাজপথ রাজপথ; বীর বাঙালি অস্ত্র ধর, বাংলাদেশ স্বাধীন কর; ঘরে ঘরে দূর্গ গড়, বাংলাদেশ স্বাধীন কর।’ বঙ্গবন্ধু তাঁর ঐতিহাসিক ভাষণে ২৫ মার্চ ঢাকায় জাতীয় পরিষদের নির্ধারিত অধিবেশনে যোগদানের জন্য তিনটি শর্ত ঘোষণা করেন : (১) সামরিক আইন প্রত্যাহার করতে হবে। (২) সামরিক বাহিনীকে ব্যারাকে ফিরিয়ে নিতে হবে, বেসামরিক জনতার উপর সেনাবাহিনীর বর্বর অত্যাচার তদন্ত করতে হবে এবং (৩) অনতিবিলম্বে প্রাদেশিক সরকার গঠন করার ঘোষণা দিতে হবে। তিনি ঘোষণা দেন যে, যতক্ষণ এসব দাবি মানা না হবে ততক্ষণ পর্যন্ত সকল অফিস আদালত, শিক্ষা প্রতিষ্ঠান বন্ধ থাকবে এবং সরকারের সাথে কোনরকমের সহযোগিতা প্রদান করা হবে না। বঙ্গবন্ধু আরোও ষোষণা দেন যে, দেশ পুরোপুরি মুক্ত না হওয়া পর্যন্ত এই অবৈধ সরকারকে কোনো রাজস্ব কর দেয়া হবে না এবং পূর্ববাংলা থেকে পশ্চিম পাকিস্তানে কোনো টাকা পাঠানো যাবে না। তিনি প্রত্যেকটি গৃহকে প্রতিরোধের দুর্গে পরিণত করার এবং যার যা হাতিয়ার আছে তা দিয়ে শত্রুর মোকাবেলা করার জন্য জনগণকে নির্দেশ দেন। বঙ্গবন্ধু তাঁর ভাষণ শেষ করেন এই বলে: ‘এবারের সংগ্রাম আমাদের মুক্তির সংগ্রাম, এবারের সংগ্রাম আমাদের স্বাধীনতার সংগ্রাম। জয় বাংলা।’<br /><br />

৯ মার্চ  পল্টন ময়দানে একটি বিশাল জনসভায় মওলানা আবদুল হামিদ খান ভাসানী ও আতাউর রহমান খান স্বাধীনতা ঘোষণা করেন।<br /><br />

১০ মার্চ  আতাউর রহমান খান শেখ মুজিবকে অনতিবিলম্বে একটি অন্তবর্তীকালীন স্বাধীন বাংলাদেশ সরকার গঠন করার আহবান জানান।<br /><br />

১১ মার্চ  ইপিসিএস এবং সিএসপি সমিতির বাঙালি সদস্যরা বঙ্গবন্ধুর প্রতি তাঁদের আনুগত্য ঘোষণা করেন এবং প্রত্যেকের একদিনের বেতন আওয়ামী লীগের তহবিলে দান করেন।<br /><br />

১৪ মার্চ  সামরিক বাহিনীর রসদ সরবরাহ বন্ধ এবং পশ্চিম পাকিস্তানে মালামাল যেতে না দেয়ার উদ্দেশ্যে নগরীর বেশ কিছু গুরুত্বপূর্ণ স্থানে চেকপোস্ট বসিয়ে স্বাধীন বাংলা ছাত্র সংগ্রাম পরিষদ প্রতিরোধের প্রস্ত্ততি নিতে শুরু করে।<br /><br />

১৫ মার্চ  প্রেসিডেন্ট ইয়াহিয়া খান সিনিয়র জেনারেল এবং অফিসারদের নিয়ে ঢাকায় আগমন করেন। একই দিনে স্বাধীন বাংলা ছাত্র সংগ্রাম পরিষদ ঘোষণা দেন যে, বাংলাদেশ ইতিমধ্যে স্বাধীন হয়ে গেছে এবং তাদের জন্য বিধিনিয়ম তৈরি করার কোনো আইনসিদ্ধ অধিকার সামরিক বাহিনীর নেই। তারা আরো ঘোষণা দেন যে, বাংলাদেশ অন্য কারো নয়, শুধুমাত্র বঙ্গবন্ধু শেখ মুজিবুর রহমানের আদেশ মেনে চলবে। তারা আসন্ন একটি সশস্ত্র সংগ্রামে শরীক হওয়ার জন্য বাংলাদেশের সব নাগরিককে আহবান জানান।<br /><br />

১৬ মার্চ  মুজিবের সাথে ইয়াহিয়ার সংলাপ শুরু হয় যা মাঝেমধ্যে বিরতিসহ ২৫ মার্চ পর্যন্ত চলার কথা।<br /><br />

১৮ মার্চ  স্বাধীন বাংলা ছাত্র সংগ্রাম পরিষদ বাংলাদেশের স্বাধীনতার নিয়মতান্ত্রিক সংগ্রামের প্রতি সমর্থন জ্ঞাপন এবং বাংলাদেশ ত্যাগে পাকিস্তানের সামরিক সরকারের উপর চাপ সৃষ্টির জন্য বিশ্বসমাজের প্রতি আহবান জানান।<br /><br />

১৯ মার্চ  গাজীপুর সমরাস্ত্র কারখানা এবং জয়দেবপুর ক্যান্টনমেন্টে পূর্ব ও পশ্চিম পাকিস্তানি সৈনিকদের মধ্যে খন্ডযুদ্ধ। বিক্ষোভরত জনতার উপর গুলি চালাতে ইস্ট বেঙ্গল রেজিমেন্টের অস্বীকৃতি। পাকিস্তানি সামরিক বাহিনী যাতে যানবাহন নিয়ে চলাফেরা করতে না পারে সে জন্য জনতা কর্তৃক বিভিন্ন গুরুত্বপূর্ণ স্থানে সড়কে ব্যারিকেড স্থাপন। ব্যারিকেড দিতে গিয়ে পাকিস্তান সেনাবাহিনীর গুলিতে বেশ কয়েকজন কর্মী নিহত।<br /><br />

২০ মার্চ  চট্টগ্রামে এক সংবাদ সম্মেলন ডেকে মওলানা ভাসানী শেখ মুজিবকে প্রধান করে একটি অন্তর্বর্তী সরকার গঠন করার জন্য জেনারেল ইয়াহিয়ার প্রতি আহবান জানান। তিনি বলেন, এই অন্তর্বর্তীকালীন সরকার ঠিক করবে ভবিষ্যতে স্বাধীন বাংলাদেশ পাকিস্তানের সাথে কি ধরনের সম্পর্ক বজায় রাখবে।<br /><br />

২০-২১ মার্চ  বঙ্গবন্ধু শেখ মুজিবুর রহমান এবং অন্যান্য প্রধান নেতা ইয়াহিয়া খানের সাথে দেখা করেন এবং বেশ কয়েকবার তাঁর সাথে দীর্ঘ আলোচনায় মিলিত হন। কিন্তু তাতে লক্ষণীয় কোনো অগ্রগতি অর্জিত হয় নি।<br /><br />

২২ মার্চ  প্রতিটি জাতীয় সংবাদপত্রে বাংলাদেশের জাতীয় পতাকার ছবি প্রকাশিত হয়। স্বাধীন বাংলা ছাত্র সংগ্রাম পরিষদ প্রথাগত পাকিস্তান দিবস পালন প্রত্যাখ্যান করার এবং এর পরিবর্তে প্রতিটি বাড়ির ছাদে বাংলাদেশের পতাকা উত্তোলন করার নির্দেশ দেন।<br /><br />

২৩ মার্চ  কেন্দ্রীয় ছাত্র সংগ্রাম পরিষদের (নূরে আলম সিদ্দিকী, শাহজাহান সিরাজ, এএসএম আবদুর রব এবং আবদুর কুদ্দুস মাখন) নেতৃত্বে্ বাঙালি জাতীয়তাবাদীরা পাকিস্তান দিবস প্রত্যাখ্যান করেন। স্বাধীন বাংলা কেন্দ্রীয় শ্রমিক পরিষদ বঙ্গবন্ধুর বাড়ির ছাদে বাংলাদেশের জাতীয় পতাকা উত্তোলন করেন। জয়বাংলা বাহিনী পল্টন ময়দানে স্বাধীনতা দিবসের কুচকাওয়াজের আয়োজন করেন। কেন্দ্রীয় ছাত্র সংগ্রাম পরিষদের চার নেতা স্বাধীনতার কুচকাওয়াজের অভিবাদন গ্রহণ করেন। এ সময় পূর্ব রেকর্ডকৃত ‘আমার সোনার বাংলা’ গানটি জাতীয় সঙ্গীত হিসেবে বাজানো হয়। স্বাধীন বাংলাদেশ ছাত্র সংগ্রাম পরিষদের নেতৃত্বে জয়বাংলা বাহিনীর দশ প্লাটুন এবং একটি ব্যান্ড প্লাটুন পল্টন ময়দান হতে কুচকাওয়াজ করে বঙ্গবন্ধুর বাসভবনে পৌছায়। বঙ্গবন্ধু তাদের উদ্দেশে ভাষণ দেন এবং চূড়ান্ত বিজয় পর্যন্ত তাদের প্রস্ত্তত থাকার নির্দেশ দেন।<br /><br />

২৪ মার্চ  যশোরে ইস্ট পাকিস্তান রাইফেলস (ইপিআর)-এর হেডকোয়ার্টারে সিপাহীরা জয়বাংলা শ্লোগান দেন এবং বাংলাদেশের পতাকা উত্তোলন করে অভিবাদন জানান।<br /><br />

২৫ মার্চ  শ্রমিক ফেডারেশন এবং বিপ্লবী ছাত্র ইউনিয়ন আয়োজিত সভায় সভাপতির ভাষণে কাজী জাফর আহমদ ইয়াহিয়া-মুজিব সংলাপকে একটি ধোকা বলে আখ্যায়িত করেন এবং সশস্ত্র সংগ্রামের মাধ্যমে ‘স্বাধীন গণপ্রজাতন্ত্রী বাংলাদেশ’ প্রতিষ্ঠার আহবান জানান। বন্দর শ্রমিক ও কর্মকর্তারা করাচী থেকে অস্ত্রশস্ত্র বোঝাই হয়ে চট্টগ্রামে আগত সোয়াত জাহাজ থেকে মালামাল খালাস করতে অস্বীকৃতি জানায়। পাকিস্তানি সৈন্যদের চলাচলে বাধা সৃষ্টির জন্য চট্টগ্রামবাসী সকল প্রধান সড়কে ব্যারিকেড দেন। ইয়াহিয়া খান এবং পশ্চিম পাকিস্তানি নেতারা সঙ্গোপনে ঢাকা ত্যাগ করেন। মধ্যরাতে ঢাকায় পাকিস্তানি বাহিনীর হামলায় হাজার হাজার মানুষ নিহত হয়।<br /><br />

স্বাধীনতা ঘোষণা

২৬ মার্চ একটি বার্তার আকারে বঙ্গবন্ধু শেখ মুজিবুর রহমান স্বাধীনতার ঘোষণা জারি করেন। ২৫ মার্চ মধ্যরাতের অব্যবহিত পর এ ঘোষণাটি ইপিআর-এর নিকট পৌছানো হয় এবং তা ইপিআর বেতারের মাধ্যমে যথাযথভাবেই প্রচার করা হয়। তবে প্রচার ব্যবস্থাটি ঠঐঋ ফ্রিকোয়েন্সি ক্রিস্টাল নিয়ন্ত্রিত ছিল বলে খুব বেশি সংখ্যক লোক ঘোষণাটি শুনতে পায় নি। বঙ্গবন্ধুর ঘোষণা ছিল নিম্নরূপ: ‘এটাই হয়তো আমার শেষ বার্তা। আজ থেকে বাংলাদেশ স্বাধীন। বাংলাদেশের জনগণের প্রতি আমার আহবান, আপনারা যে যেখানেই থাকুন এবং যার যা কিছু আছে তা দিয়ে শেষ পর্যন্ত দখলদার সেনাবাহিনীকে প্রতিহত করুন। বাংলাদেশের মাটি থেকে পাকিস্তান দখলদার বাহিনীর শেষ সৈনিকটি বিতাড়িত এবং চূড়ান্ত বিজয় অর্জিত না হওয়া পর্যন্ত আপনাদের এ লড়াই চালিয়ে যেতে হবে।’<br /><br />

ইস্ট বেঙ্গল রেজিমেন্টের মেজর জিয়াউর রহমান পরদিন (২৭ মার্চ) চট্টগ্রাম কালুরঘাট বেতারকেন্দ্র থেকে স্বাধীনতার ঘোষণা দেন:<br /><br />

‘আমি মেজর জিয়া, বাংলাদেশ লিবারেশন আর্মির অস্থায়ী প্রধান সেনাপতি এতদ্বারা শেখ মুজিবুর রহমানের পক্ষ থেকে বাংলাদেশের স্বাধীনতা ঘোষণা করছি।<br /><br />

‘আমি আরও ঘোষণা করছি, আমরা ইতোমধ্যে শেখ মুজিবের নেতৃত্বে একটি সার্বভৌম ও বৈধ সরকার গঠন করেছি, যে সরকার আইনবিধান ও শাসনতন্ত্র অনুযায়ী রাষ্ট্র পরিচালনায় অঙ্গীকারাবদ্ধ। নতুন গণতান্ত্রিক সরকার আন্তর্জাতিক সম্পর্কের ক্ষেত্রে জোটনিরপেক্ষ নীতিতে প্রতিশ্রুতিবদ্ধ। এই সরকার সব দেশের সঙ্গে বন্ধুত্বপূর্ণ সম্পর্ক গড়ে তোলার এবং আন্তর্জাতিক শান্তি প্রতিষ্ঠার চেষ্টা করবে। আমি সকল দেশের সরকারকে বাংলাদেশে নৃশংস গণহত্যার বিরুদ্ধে তাদের নিজ নিজ দেশে জনমত গড়ে তোলার আহবান জানাই। শেখ মুজিবুর রহমানের নেতৃত্বাধীন সরকার বাংলাদেশের সার্বভৌম ও বৈধ সরকার এবং এ সরকার বিশ্বের সব গণতান্ত্রিক দেশের স্বীকৃতি পাওয়ার যোগ্য।’<br /><br />

৩০ মার্চ মেজর জিয়াউর রহমান স্বাধীন বাংলা বেতার কেন্দ্র থেকে আবারও ঘোষণা দেন যে, ‘পাকিস্তানের সেনাবাহিনী, বিমান ও নৌবাহিনী বেসামরিক জনগণের বিরুদ্ধে একটি সম্মিলিত আক্রমণ শুরু করেছে এবং নির্বিচারে মানুষ হত্যা করছে।’ তিনি তাঁর ঘোষণায় বলেন, ‘আমি এই পরিস্থিতিতে হস্তক্ষেপ এবং আমাদের সাহায্যে এগিয়ে আসার জন্য আবারও জাতিসংঘ ও বৃহৎ শক্তিবর্গের প্রতি আহবান জানাচ্ছি। বিলম্ব করা হলে আরও লাখ লাখ লোক নির্মম হত্যার শিকার হবে।’<br /><br />

১০ এপ্রিল  জাতীয় পরিষদ এবং প্রাদেশিক পরিষদের অনেক নির্বাচিত সদস্য কোলকাতায় সমবেত হয়ে একটি প্রবাসী গণপরিষদ গঠন এবং স্বাধীনতার ঘোষণার খসড়া রচনা করেন। ১৭ এপ্রিল বৈদ্যনাথতলায় (ঘোষণার পর এর নামকরণ হয় মুজিবনগর) আনুষ্ঠানিকভাবে এই ঘোষণা গৃহীত হয়। জাতীয় পরিষদ সদস্য (এমএনএ) অধ্যাপক ইউসুফ আলী স্বাধীনতার ঘোষণা পাঠ করেন। এ ঘোষণার প্রথম অংশে মুক্তিযুদ্ধের পটভূমি ব্যাখ্যা করা হয়। এরপর তিনি পাঠ করেন:<br /><br />

‘আমরা বাংলাদেশের জনগণের নির্বাচিত প্রতিনিধিরা সর্বময় ও চূড়ান্ত মতের অধিকারী বাংলাদেশের জনগণের ম্যান্ডেট দ্বারা ক্ষমতাপ্রাপ্ত হয়ে যথাযথভাবে একটি গণপরিষদ গঠন করেছি এবং বাংলাদেশের জনগণের জন্য সাম্য, মানবিক মর্যাদা, সামাজিক ন্যায়বিচার নিশ্চিত করার লক্ষ্যে পারস্পরিক আলাপ-আলোচনাক্রমে বাংলাদেশকে সার্বভৌম গণপ্রজাতন্ত্র হিসেবে ঘোষণা ও গঠন করেছি এবং এর মাধ্যমে বঙ্গবন্ধু শেখ মুজিবুর রহমানের ইতিপূর্বেকার স্বাধীনতা ঘোষণাকে অনুমোদন করছি এবং সত্যায়ন ও অঙ্গীকার করছি যে, শাসনতন্ত্র প্রণীত হওয়ার পূর্ব পর্যন্ত বঙ্গবন্ধু শেখ মুজিবুর রহমান প্রজাতন্ত্রের প্রেসিডেন্ট এবং সৈয়দ নজরুল ইসলাম প্রজাতন্ত্রের ভাইস প্রেসিডেন্ট হিসেবে দায়িত্ব পালন করবেন এবং প্রেসিডেন্ট হবেন প্রজাতন্ত্রের সকল সশস্ত্র বাহিনীর সর্বাধিনায়ক ...।’<br /><br />

ঘটনা প্রবাহের বিশ্লেষণ পাকিস্তানের প্রারম্ভিক দিনগুলো থেকে শুরু করে ১৯৬৬ সালে আওয়ামী লীগ প্রধান শেখ মুজিবুর রহমানের ৬-দফা কর্মসূচি ঘোষণা পর্যন্ত সময়ের ঘটনা প্রবাহ, ১৯৭০ সালের সাধারণ নির্বাচনে জনগণের প্রদত্ত রায়, তৎপরবর্তী ঘটনা পরম্পরা এবং পরিশেষে স্বাধীনতা ঘোষণার বিশ্লেষণ করলে অনুধাবন করা যায় যে, পাকিস্তান জাতীয়তাবাদের ভিত্তি হিসেবে মুসলিম জাতীয়তাবাদ পাকিস্তান প্রতিষ্ঠার পরের বছর থেকেই দুর্বল থেকে দুর্বলতর হতে থাকে, এবং দিনে দিনে রূপলাভ করে বাংলাদেশ জাতীয়তাবাদের ধারণা এবং সে ধারণা ১৯৭০ সালের সাধারণ নির্বাচনের পর থেকে ক্রমশ বেগবান হতে থাকে।<br /><br />

১৯৭০ সালের ডিসেম্বরের নির্বাচনের রায়ে শেখ মুজিব বাঙালিদের একমাত্র মুখপাত্রে পরিণত হন। ছয় দফার ভিত্তিতে পাকিস্তানের শাসনতন্ত্রকে ঢেলে সাজানোর জন্য জনগণের ম্যান্ডেট পেয়েছিলেন শেখ মুজিবুর রহমান। নির্বাচনের পর ছয়দফা ফর্মুলা জনসমর্থিত হয়ে একটি বৈধ দলিলে রূপান্তরিত হয় এবং তা গোটা বাঙালি জাতির ‘বিল অফ রাইটস’-এ পরিণত হয়। ফরাসী বিপ্লবের প্রাক্কালে টেনিস কোর্টে জনপ্রতিনিধিদের শপথ গ্রহণের মধ্য দিয়ে থার্ড এস্টেট যে অবস্থায় দাঁড়িয়েছিল তেমনিভাবে ম্যান্ডেট দ্বারা সমর্থিত হয়ে নির্বাচিত প্রতিনিধিরা রমনা রেসকোর্সে এ মর্মে শপথ নিলেন যে, পাকিস্তানের নতুন শাসনতন্ত্র প্রণয়নের সময় তারা কখনই ৬-দফা ফর্মূলা এবং ১১-দফা কর্মসূচি থেকে বিচ্যুত হবেন না।<br /><br />

শপথ দিবস (৩ জানুয়ারি ১৯৭১) থেকে শুরু করে তথাকথিত সংলাপের শেষ মুহূর্ত পর্যন্ত গোটা নির্বাচনোত্তর অধ্যায়ে জনগণের নির্বাচিত একচ্ছত্র নেতা শেখ মুজিব ছয় দফা ও এগারো দফার পক্ষে অটল থাকেন। তাঁর স্বপ্ন ছিল পাকিস্তানকে লাহোর প্রস্তাবের (১৯৪০) মর্মবাণী অনুযায়ী একটি ফেডারেশনে পরিণত করা। পরবর্তীকালে জনগণের নির্বাচিত প্রতিনিধিদের অনুমতি ছাড়াই মূল লাহোর প্রস্তাবের ‘রাষ্ট্রসমূহ’ (states) শব্দটিকে ‘রাষ্ট্র’ (state) শব্দে রূপান্তরিত করা হয়। শেখ মুজিব এটাকে সংশোধন করতে চেয়েছিলেন এবং পাকিস্তানের অধিরাষ্ট্রের (supra state) কাঠামোর মধ্যে রাষট্র নয় ‘রাষ্ট্রসমূহ’ প্রতিষ্ঠা করতে চেয়েছিলেন।<br /><br />

শীঘ্রই মুজিব তাঁর ফেডারেশন রাষ্ট্রের ধারণার ব্যাপারে সম্মিলিত ছাত্র নেতৃবৃন্দের তরফ থেকে কিছু তত্ত্বগত মত পার্থক্যের সম্মুখীন হলেন। ছাত্রদের মধ্যে এ বিশ্বাসের প্রবণতা ছিল যে মুজিবের পরিকল্পনা ছিল বড় বেশি কল্পনাপ্রবণ যার বাস্তবায়ন অসম্ভব। তারা বাঙালি জাতীয়তাবাদের ভিত্তিতে একটি সমান্তরাল রাজনৈতিক মতবাদের জন্ম দিয়েছিল। বাঙালিদের জন্য তারা একটি নতুন সত্ত্বার স্বপ্ন দেখে : পদ্মা মেঘনা যমুনা, তোমার আমার ঠিকানা, আর তা ছিল এক নতুন জাতীয়তা। কিন্তু এ ধরনের একটি জাতীয়তাবাদী মতাদর্শ গড়ে তোলার অর্থ প্রকৃতপক্ষে তাদের নেতার মূল রাজনৈতিক দর্শনকে চ্যালেঞ্জ করা ছিল না। এটা ছিল শুধুমাত্র একটি বিকল্প কর্মসূচি যা নেতার মৌন সম্মতিতে অনুসৃত হচ্ছিল। যদি ৬-দফা ব্যর্থ হয়, তাহলে ১-দফা অবশ্যই জয়যুক্ত হবে এবং তা হলো স্বাধীনতা। তাঁর অনন্য জনমোহিনী ব্যক্তিত্ব এবং নিপুণ কুশলীপনার দ্বারা মুজিব একদিকে নতুন প্রজন্মের জঙ্গী জাতীয়তাবাদী আশা-আকাঙ্খা এবং অন্যদিকে মধ্যপন্থী ফেডারেল ভাবধারার অনুসারীদের মধ্যে একটি ভারসাম্য রক্ষা করতে সক্ষম হন।<br /><br />

১৯৭১ সালের ১ মার্চ থেকে জাতীয়তাবাদী শক্তি এবং রাজনীতি সচেতন জনগোষ্ঠীর কাছে স্ফটিকের মতো পরিস্কার হয়ে যায় যে, পাকিস্তানের মিলিটারি এবং রাজনৈতিক কায়েমী স্বার্থবাদী গোষ্ঠী নির্বাচনী রায় মেনে নিতে অনিচ্ছুক। এটা খুব সহজে বুঝা যাচ্ছিল যে, পূর্ব পাকিস্তানের সঙ্গে কয়েক দশক ধরে পশ্চিম পাকিস্তানের আধিপত্যমূলক সম্পর্ক এমন জটিল আকার ধারণ করেছিল যে, পাকিস্তানের সামরিক বাহিনী এবং বেসামরিক এলিট শ্রেণীর পক্ষে তাদের কর্তৃত্বপূর্ণ অবস্থান থেকে নিচে নেমে আসা বাস্তবে অসম্ভব হয়ে দাঁড়িয়েছিল এবং বাঙালিদের সাথে সমতার ভিত্তিতে সম্পর্ক পুনঃনির্ধারণে রাজি হওয়া তাদের পক্ষে কার্যত ছিল অসম্ভব। তাদের কাছে ৬-দফা ফর্মুলা বাস্তবায়নের অর্থ ছিল বাঙালিদের প্রথমত পাকিস্তানি একচ্ছত্র আধিপত্য ধুলিস্যাৎ করার মাধ্যমে পাকিস্তান ভেঙ্গে দেয়া। তাদের জন্য কঠিন বাস্তবতা ছিল, সম্ভব হলে পূর্ব পাকিস্তানের সাথে বর্তমান ক্ষমতার কাঠামো অক্ষুণ্ণ রেখে অগ্রসর হওয়া এবং উদ্ধারকাজ ব্যর্থ হলে ডুবন্ত নৌকাটি অর্থাৎ পূর্ব পাকিস্তান পরিত্যাগ করা।<br /><br />

এ অবস্থায় জাতীয়তাবাদী শক্তি বিশেষত ছাত্র নেতৃত্ব তাদের মতাদর্শ বাস্তবায়নে এগিয়ে এলো এবং সশস্ত্র সংগ্রামের মাধ্যমে পূর্ব পাকিস্তানকে স্বাধীন করার সিদ্ধান্ত নিল। ১৯৬৯ সালের প্রথমদিকের গণঅভ্যুত্থান ছিল এই সংগ্রামের একটি মোক্ষম মহড়া। যুদ্ধসম গণঅভ্যুত্থানের মধ্য দিয়ে শেখ মুজিবের মুক্তি আদায়ের মাধ্যমে প্রতিকী অর্থে পাকিস্তান রাষ্ট্রের ধারণার কবর রচিত হয় এবং শুরু হয় বাঙালি জাতীয়তাবাদ বাস্তবায়নের আনুষ্ঠানিক যাত্রা, যার প্রাথমিক যাত্রা শুরু হয়েছিল ভাষা আন্দোলন থেকে। ১৯৬৯ সালের গণঅভ্যুত্থানের সময় রাজনীতিতে ছাত্র এবং যুব নেতৃত্বের উত্থান ঘটে। অধিকাংশ প্রবীণ নেতা তখন জেলে অন্তরীণ। এমতাবস্থায়, ছাত্র ও যুব সমাজই জনগণকে সংগঠিত করে ইপ্সিত লক্ষ্যে পৌঁছাবার দায়িত্ব গ্রহণ করে। তারা স্কুল কলেজ ও বিশ্ববিদ্যালয়ে সংগ্রাম পরিষদ গঠন করেন। সকল কৃষক ও শ্রমিক ফ্রন্ট সংগ্রাম পরিষদ গঠন করেন এবং সংগ্রামী মনোভাব চাঙ্গা করতে অনেক জাতীয়তাবাদী শ্লোগান ও প্রতীকী উদ্ভাবন করেন।<br /><br />

১৯৭১ সালের ১ মার্চ থেকে জাতিগঠনের প্রক্রিয়াগুলো গভীরতর রূপ নেয় এবং অপ্রতিরোধ্যভাবে গতিশীল হয়ে উঠে। তখন থেকে সার্বিক রাজনৈতিক পরিস্থিতি এমন মোড় নেয় যে, জনগণের মেজাজ ৬-দফা থেকে সরে এসে ছাত্রদের এক দফায় মিলিত হয়। আপাতদৃষ্টিতে ঘটনারাজি স্ববিরোধী মনে হতে পারে এজন্য যে, শেখ মুজিব যখন আলোচনার মাধ্যমে সমস্যার শান্তিপূর্ণ সমাধানের চেষ্টা করছিলেন তখন তাঁর অনুসারী ছাত্র নেতৃত্ব সরাসরি স্বাধীনতা ঘোষণার জন্য তাঁর উপর চাপ প্রয়োগ করে আসছিল। বঙ্গবন্ধুর ৬-দফার রাজনীতি এবং ছাত্রদের সরাসরি স্বাধীনতার রাজনীতি বস্ত্ততপক্ষে একে অপরের পরিপূরক। ছাত্রদের বঙ্গবন্ধুর প্রয়োজন ছিল তাঁর সহায়ক প্রেসার গ্রুপ হিসেবে এবং বঙ্গবন্ধুকে ছাত্রদের প্রয়োজন ছিল তাদের ছত্রছায়ারূপে। মুজিব যে ছাত্রদের র‌্যাডিক্যাল বা উগ্র জাতীয়তাবাদী ধারণা গ্রহণ করতে অনিচ্ছুক ছিলেন না তার প্রমাণ হলো আগরতলা ষড়যন্ত্র মামলায় মুক্তির পর থেকে তিনি বিভিন্ন জনসভায় প্রদত্ত ভাষণে ছাত্রদের উদ্ভাবিত জাতীয়তাবাদী শ্লোগানসমূহ সাগ্রহে ও প্রবল উৎসাহে ব্যবহার করেছিলেন। ছাত্র নেতৃত্বের কাছ থেকে তিনি গর্বের সাথে বঙ্গবন্ধু নামে জাতীয়তাবাদী উপাধি গ্রহণ করেছিলেন। গগণবিদারী শ্লোগান ‘জয় বাংলা’ তিনি ব্যবহার করতে শুরু করেন। সব সময়ই  ৬-দফার কথা বলতে গিয়ে তিনি এর সহসঙ্গীরূপে ১১-দফার কথাও উল্লেখ করতেন। লক্ষ্য করা যেতে পারে যে, জঙ্গি জাতীয়তাবাদী ছাত্রদের উদ্ভাবিত শ্লোগান, শব্দাবলি এবং প্রতীকের সাথে বঙ্গবন্ধুর ৬-দফা ভিত্তিক রাজনৈতিক চিন্তাধারার সুস্পষ্ট মিল না থাকলেও তারা বঙ্গবন্ধুর সাথে নিয়মিত দেখা করতেন এবং তাঁর সাথে রাজনৈতিক আলাপ আলোচনা করতেন এবং নির্দেশাদি গ্রহণ করতেন। এ প্রক্রিয়ায় কখনও কোনরকম মতানৈক্য দেখা যায় নি।<br /><br />

জাতীয়তাবাদী ছাত্র, যুব ও শ্রমিক সমাজ বাঙালি জাতীয়তাবাদী ধারণাটি বিকশিত করে এর তাত্ত্বিক ভিত্তিও স্থাপন করে। সেগুলো হলো বাঙালি জাতীয়তাবাদ, সমাজতন্ত্র, ধর্ম নিরপেক্ষতা ও গণতন্ত্র। সেই সাথে তারা ইপ্সিত রাষ্ট্রের একটি নাম দেন: বাংলাদেশ। বাংলাদেশ নাম এযাবৎ সাহিত্যের এক শিথিল প্রকাশ হিসেবেই চলে আসছিল, জাতিগত ধারণা হিসেবে নয়। তারা জাতীয় শ্লোগান ‘জয়বাংলা’ উদ্ভাবন করেন। তারা তাদের নেতা শেখ মুজিবুর রহমানকে দেখেন জাতির পিতা হিসেবে, যাকে তারা এ যাবত ‘বঙ্গবন্ধ’ু নামে জাতীয়তাবাদী উপাধিতে ভূষিত করেন; তারা জাতির একটি পতাকা তৈরি করেন এবং সেটা তারা ১৯৭১ সালের ২ মার্চ তাদের প্রথম স্বাধীনতা ঘোষণার সময় ঢাকা বিশ্ববিদ্যালয়ে উত্তোলন করেন। তারা জাতির জন্য একটি জাতীয় সংগীতও নির্বাচিত করেন: ‘আমার সোনার বাংলা ...’।<br /><br />

বাংলাদেশের প্রাক ২৬ মার্চের এই অভিব্যক্তির রূপায়নগুলিকে জাতি বরাবর সম্মান প্রদর্শন করে আসছে। শুধু একটিই ব্যতিক্রম আছে, সেটা হলো স্বাধীনতা ঘোষণার প্রশ্ন। ছাত্র নেতৃত্ব, বেশিরভাগ মত ও পথের জননেতা, সংগ্রাম পরিষদ, শ্রমিক ও কৃষক নেতৃবৃন্দ ইত্যাদি একের পর এক যেসব স্বাধীনতার ঘোষণা দিচ্ছিলেন, ঐতিহাসিক দৃষ্টিকোণ থেকে বিচার করলে সেগুলি মোটেও গালভরা বাগাড়ম্বর নয়।<br /><br />

১৯৭১ সালের ১ মার্চ থেকে বাঙালি জনগণের আলোচনা ও আকাঙ্খার বিষয় হিসেবে স্বাধীনতা সর্বজনীনতা লাভ করে। বিদ্যমান সরকার সে সময়ে রাজনৈতিক দিক দিয়ে অভিষিক্ত এসব ঘোষণা ও শ্লোগানকে রাষ্ট্রদ্রোহিতা হিসেবে গণ্য করে নি। এমনকি তাদের উদ্দেশ্য ও কর্মকান্ড আইনসিদ্ধ ছিল কিনা সে প্রশ্নও সরকার করে নি, এবং করার মতো কোন সরকারও তখন ছিল না। কেননা স্থানীয় এবং জাতীয় সংগ্রাম পরিষদ বাস্তবে দেশকে নিজেদের নিয়ন্ত্রণে নিয়ে এসেছিল। সরকার তখন ক্যান্টনমেন্টের মধ্যেই সীমাবদ্ধ হয়ে পড়ে। দেশ তখন স্বাধীন বাংলা কেন্দ্রীয় ছাত্র সংগ্রাম পরিষদ, তাদের অধীন ছাত্র সংগ্রাম পরিষদ ও অন্যান্য সংগ্রাম পরিষদের নিয়ন্ত্রণে। সুতরাং ১৯৭১ সালের ২৬ মার্চকে বাংলাদেশের স্বাধীনতা দিবস হিসেবে অনুমোদন করে মুজিবনগরের নির্বাচিত ও বিপ্লবী সরকার ২৬ মার্চের আগেই যে জনগণ কার্যত স্বাধীনতা অর্জন করেছে তার আনুষ্ঠানিক অনুমোদন দিয়েছেন মাত্র। ঐতিহাসিক বাস্তবতা হলো, এই মুজিবনগর সরকার ব্যবহূত বিশেষ বিশেষ ভাষা বা শব্দাবলির সবই ১৯৬৯ সাল থেকে ছাত্র এবং যুব নেতৃত্ব উদ্ভাবন করেছিল এবং ব্যবহার করে আসছিল। দেশের নাম বাংলাদেশ, জাতির শ্লোগান জয়বাংলা, জাতির জনক এবং তাঁর উপাধি বঙ্গবন্ধু, জাতীয় পতাকা, জাতীয় সংগীত ইত্যাদি সবই অসহযোগ আন্দোলনের সময় ব্যবহার করা হয়।<br /><br />

জাতীয় স্বাধীনতা দিবস ধার্য করার ব্যাপারে মুজিবনগর সরকার দুটি ঐতিহাসিক মডেলের যেকোন একটিকে সার্থকভাবে ব্যবহার করতে পারত। একটি হলো আমেরিকান বিপ্লবের মডেল। যখন কেন্দ্রের সাথে সব প্রতিবাদ, বিক্ষোভ ও সংলাপ ব্যর্থ হলো, তখন তেরটি কলোনির নির্বাচিত প্রতিনিধিরা ৪ জুলাই ১৭৭৬ পেনসিলভেনিয়াতে মিলিত হয়ে ওই তারিখ থেকেই স্বাধীনতা ঘোষণা করল, যদিও প্রকৃত স্বাধীনতা লাভ করেছে এর আরো সাত বছর পর। বাংলাদেশের নির্বাচিত প্রতিনিধিরাও অনুরূপভাবে ১০ এপ্রিল তারিখকে স্বাধীনতা দিবস ঘোষণা করতে পারতেন যখন তারা মুজিবনগরে মিলিত হন। বিকল্প হিসেবে তারা ফরাসী বিপ্লবকে মডেল হিসেবে ব্যবহার করতে পারতেন। বিপ্লবের প্রাক্কালে ১৪ জুলাই ১৭৮৯ সালে ক্ষুব্ধ জনতা কর্তৃক বাস্তিল কারাগার (জেলখানা হিসেবে ব্যবহূত একটি পুরাতন দুর্গ) আক্রমণের দিনটাকে ফ্রান্সের জাতীয় দিবস হিসেবে ঘোষণা করা হয়েছে। বিক্ষুব্ধ জনতার কারাগার দখলের ঘটনাকে বিপ্লবীরা ফরাসী রাজতন্ত্রের অবসানের সূত্রপাত এবং জনগণের স্বাধীনতা, সাম্য এবং মৈত্রীর প্রতীক হিসেবে গ্রহণ করেছিলেন। ১৯৬৯ সালের ২২ জানুয়ারি গণঅভ্যুত্থানের মাধ্যমে শেখ মুজিবের মুক্তি আদায়ের মধ্য দিয়ে স্বৈরাচারী আইয়ুব শাহীর পতন ঘটে এবং জনগণের বিজয় সূচিত হয়। জনতার সার্বভৌম ইচ্ছাকে যদি প্রাধান্য দেওয়া যেত তাহলে বাংলাদেশের জাতি গঠনের যাত্রাপথের সূচনা হিসেবে এই দিনটাই সবচেয়ে তাৎপর্যপূর্ণ বলে গ্রহণ করে স্বাধীনতা দিবস হিসেবে গণ্য করা যেত। আরেকটি অতি প্রাসঙ্গিক দিন ৭ই মার্চ যখন বঙ্গবন্ধু শেখ মুজিবুর রহমান বস্ত্তত স্বাধীনতাই ঘোষণা করেছিলেন। জনতার ক্ষমতা এবং ইচ্ছার প্রতিফলনের আরেকটি অতি তাৎর্যপূর্ণ দিবস ছিল ২৩ মার্চ। ওই দিনে বাংলাদেশের বেশিরভাগ অফিস এবং বাড়ি-ঘরের ছাদে জাতীয় পতাকা উত্তোলন করা হয়েছিল; আনুষ্ঠানিক কুচকাওয়াজ অনুষ্ঠিত হয়েছিল; জাতীয় পতাকা উত্তোলন করার সময় জাতীয় সংগীত গাওয়া হয়েছিল এবং জাতীয় পতাকাকে আনুষ্ঠানিকভাবে অভিবাদন জানানো হয়েছিল। স্বাধীনতার সব বৈশিষ্ট্য ওই সব ঘটনায় দৃশ্যমান।<br /><br />

বাংলাদেশের সমসাময়িক ইতিহাসের একটি প্রবণতা হলো মুক্তিযুদ্ধ বিষয়ে ক্ষমতাসীনদের নিজের বক্তব্যকে ইতিহাস বলে চাপিয়ে দেয়া। সরকার পরিবর্তন হলে স্বাধীনতার ঘোষকের নাম পরিবর্তন হয়, বিশেষ করে পাঠ্য পুস্তকে। বাংলাদেশের জাতি গঠনের প্রক্রিয়াগুলো এখনও সক্রিয় এবং তেমনি সক্রিয় জননেতাদের মধ্যেকার সকল রকমের মতপার্থক্য। তবে আমাদের স্বাধীনতার ইতিহাসের অন্তত কিছু কিছু বিষয় এখন বিতর্কের অতীত, যেমন বাঙালি জাতীয়তাবাদ বিকাশে শেখ মুজিবুর রহমানের অবদান এবং স্বাধীনতার জন্য জাতীয়তাবাদী সংগ্রামের আদর্শগত ও আনুষ্ঠানিক রূপ দেওয়ার ক্ষেত্রে ছাত্র ও যুব নেতৃবৃন্দের আলোকিত ও গতিশীল সম্মিলিত নেতৃত্ব।<br /><br />  [সিরাজুল ইসলাম][..বাংলাদেশ উইকিপিডিয়া থেকে সংগৃহীত.]
